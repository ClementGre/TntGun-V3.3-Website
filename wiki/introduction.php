<br><center><h2><b>Ici, vous pourez découvrir le RolePlay de TntGun tout en détail pour une meilleur expèrience de jeu.</b></h2></center>

<br><hr><br>

<p><i class="fas fa-code"></i><b> Informations de base</b></p>
<br>
<p>Pour accéder au menu principal du serveur, vous devez faire la commande <u>/?</u> Depuis ce menu, vous aurez accès aux informations relatives au serveur et au magasin qui vous est déconseillé car le prix auquel vous vendrez est 10× plus petit que le prix d'achat. Utilisez plutôt les magasins d'<a href="?page=entreprises">entreprises</a> !</p>
<br>
<p>Il existe différentes villes en RolePlay : Arvenia, Bourg Palette, Quoted Sand, Paradise Point et Farmland dans lesquelles vous trouverez des <a href="?page=claims">claims</a> à différents prix.</p>

<br><hr><br>

<p><i class="far fa-star"></i><b> Les spécialités du serveur</b></p>
<br>
<p>Le RolePlay de TntGun repose essentiellement sur 3 grands principes :</p>
<p>- <a href="?page=claims">Les claims</a></p>
<p>- <a href="?page=entreprises">Le système d'entreprises</a></p>
<p>- <a href="?page=compétences">Les compétences</a></p>
<br>
<p><a href="?page=claims">Les claims</a> sont des terrains que vous pouvez acheter pour construire votre maison, votre entreprise ou encore votre farm. Il en existe diférents types.</p>
<p><a href="?page=entreprises">Le système d'entreprises</a> vous offre la possibilité de créer votre entreprise, de recruter des salariés, de les payer et de créer un magasin avec un système de pancartes.</p>
<p><a href="?page=compétences">Les compétences</a> consistent en différentes activités (Bucheron, Mineur, Fermier...) que vous pouvez pratiquer pour gagner des % dans cette compétence. Arrivé à 50%, vous pourrez débloquer des crafts spéciaux qui deviendront de plus en plus abusés vers les 100%.</p>