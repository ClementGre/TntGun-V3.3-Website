<br><center><h2><b>Pour construire votre maison ou toute autre structure, vous devrez acheter des claims (terrains)</b></h2></center>

<br><hr><br>

<p><i class="fas fa-cubes"></i><b> Les types de claims</b></p>
<br>
<p>Il existe 4 types de claims :</p>
<p>- <u>Les claims libres</u> : vous pouvez construire sur toute la hauteur de la map et faire ce que vous voulez.</p>
<p>- <u>Les Appartements</u> : comme son nom l'indique, c'est un appartement !</p>
<p>- <u>Les zones agricoles</u> : réservé à l'agriculture, ce type de claim se situe principalement dans la ville Farmland.</p>
<p>- <u>Les industries</u> : ce type de claim s'achète avec votre <a href="?page=entreprises">entreprise</a>, il permet de vendre des items.</p>
<br>
<p>Les claims 2D (qui font toute la hauteur de la map) coûtent 40€ par m² et les claims 3D (Appartements...) sont à 5€ par m³ soit 20€ par m² pour un appartement de 4 blocs de hauteur. Cependant, le prix des claims dépendent toujours de la ville dans laquelle vous vous situez.</p>
<p>Vous pouvez voir tous les claims sur la map Minecraft dans votre navigateur internet grace à la <a href="http://149.202.86.50:1176/?worldname=world&mapname=surface&zoom=5&x=-16&y=64&z=4">dynmap</a>.</p>
<br>
<p>Vous ne pourrez pas avoir plus de deux claims de chaque type.</p>

<br><hr><br>

<p><i class="fas fa-terminal"></i><b> Les commandes</b></p>
<br>
<p>Voici les commandes sur les claims :</p>
<p>- <u>/claim sell &ltprix/auto&gt</u> : met en vente votre claim,</p>
<p>- <u>/claim sellCountry</u> : vendre son claim à l'État,</p>
<p>- <u>/claim invite &ltjoueur&gt</u> : invite un joueur dans votre claim,</p>
<p>- <u>/claim kick</u> : expulse tout les invités de votre claim,</p>
<p>- <u>/claim quit</u> : quitte le claim si vous êtes un invité.</p>

<br><hr><br>

<p><i class="fas fa-university"></i><b> Les villes</b></p>
<br>
<p>Il existe différentes villes en RolePlay :</p>
<p>- <u>Arvenia</u> (Tout types de claims)</p>
<p>- <u>Bourg Palette</u> (Tout types de claims)</p>
<p>- <u>Smart Speed</u> (Tout types de claims)</p>
<p>- <u>Quoted Sand</u> (Appartements pas cher)</p>
<p>- <u>Paradise Point</u> (Claims libres)</p>
<p>- <u>Farmland</u> (Zones agricoles)</p>
<br>
<p>Chacune de ces villes offrent différents types de claims avec différents prix. Vous pouvez vous y téléporter avec la commande /spawn &ltnom&gt</p>

<br><hr><br>

<p><i class="fas fa-tags"></i><b> Autre</b></p>
<br>
<p>Vous pouvez recevoir jusqu'a 5 000€ par mois si vous avez un claim avec des belles structures pour vous récompenser de vos builds.</p>
<p>Il est strictement interdit de construire une farm, une base ou stocker des items en dehors de vos claims.</p>