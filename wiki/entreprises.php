<br><center><h2><b>Vous pouvez créer votre entreprise pour 30 000€ afin de lancer votre buisness !</b></h2></center>

<br><hr><br>

<p><i class="fas fa-clipboard-check"></i><b> Le principe</b></p>
<br>
<p>Votre entreprise vous permettra de vendre des items aux autres joueurs via des pancartes. De plus, vous obtiendrez toutes les semaines, si vous avez fait beaucoup de ventes, de l'argent en bonus ce qui doublera presque votre chiffre d'affaire.</p>
<br>
<p>Vous pouvez uniquement placer vos pancartes de vente dans un <a href="?page=claims">claim</a> de type industrie que vous achèterez avec l'argent de votre entreprise. Tout les managers de votre entreprise pourront construire dans ce terrain et les salariés pourront uniquement ouvrir les portes et les coffres mais vous pourez les surveiller avec la commande /ent log</p>

<br><hr><br>

<p><i class="fas fa-dollar-sign"></i><b> Le système de vente</b></p>
<br>
<p>Vous pouvez récupérer une pancarte de vente avec la commande <u>/ent sign &ltprix&gt &ltdescription</u> Cette commande vous donnera une pancarte qui vendra l'item que vous avez en main au prix entré.</p>
<br>
<p>- Vous pouvez placer cette pancarte sur un coffre pour vendre l'item s'il est présent dans le coffre.</p>
<p>- Vous pouver placer cette pancarte à n'importe quel autre endroit pour qu'il prenne l'item dans le(s) coffre(s) de ressources.</p>
<br>
<p>Pour ajouter un coffre de ressources, placez vous à coté du coffre en question puis exécutez la commande <u>/ent src</u>. Toutes les pancartes qui ne seront pas posées sur un coffre prendront leurs items dans ce(s) coffre(s).</p>

<br><hr><br>

<p><i class="fas fa-terminal"></i><b> Les commandes pour les particuliers</b></p>
<br>
<p>Voici les commandes sur les claims :</p>
<p>- <u>/ent create &ltnom&gt</u> : Créer son entreprise, coute 30 000€ dont 5 000€ qui sera versé sur le compte de votre entreprise</p>
<p>- <u>/ent leave </u> : Quitter son entreprise ou la dissoudre si on est PDG</p>
<p>- <u>/ent list</u> : Voir la liste des entreprises du serveur</p>
<p>- <u>/ent info &ltentreprise&gt</u> : Avoir des informations sur une entreprise</p>
<p>- <u>/ent money</u> : Voir l'argent de son entreprise</p>
<br>
<p><i class="fas fa-terminal"></i><b> Les commandes pour les manager et PDG</b></p>
<br>
<p>- <u>/ent hire &ltjoueur&gt &ltaucun/salarié/manager&gt</u> : embaucher, licencier, rétrograder ou promouvoir un joueur</p>
<p>- <u>/ent pay &ltjoueur&gt/ent:&ltentreprise&gt</u> : payer un joueur ou une entreprise avec le préfixe "ent:"</p>
<p>- <u>/ent sign &ltprix&gt &ltdescription</u> : obtenir une pancarte pour vendre l'item que vous avez dans la main</p>
<p>- <u>/ent src [clear]</u> : définir un coffre de ressource (ou reset les coffres de ressources)</p>
<p>- <u>/ent log &ltjoueur&gt</u> : voir les blocs ramenés/emportés dans les coffres de l'entreprise par un salarié</p>
<p>- <u>/ent rename &ltnom&gt</u> : renommer son entreprise</p>