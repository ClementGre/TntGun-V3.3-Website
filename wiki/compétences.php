<br><center><h2><b>Farmez vos compétences pour atteindre les 100% et débloquer des items abusés <i class="far fa-smile-wink"></i></b></h2></center>

<br><hr><br>

<p><i class="fas fa-clipboard-check"></i><b> Le principe</b></p>
<br>
<p>Il existe 5 métiers que vous pouvez farmer en même temps. Votre avancée dans les métiers se caractérise par un pourcentage : vous commencez à 0%, et arrivés à 50% vous débloquez des items spéciaux qui deviendront de plus en plus abusés vers les 100%.</p>
<br>
<p>Différents métiers :</p>
<p>- <u>Mineur</u> : minez des minerais et débloquez une pioche qui casse plusieurs blocs à la fois</p>
<p>- <u>Bucheron</u> : cassez des buches et débloquez une hache qui casse les arbres en un seul coup</p>
<p>- <u>Fermier</u> : farmez des plantes et débloquez une houe qui plante et casse plusieurs plantes à la fois</p>
<p>- <u>Chasseur</u> : tuez des entités et débloquez le craft de certains spawners</p>
<p>- <u>Enchanteur</u> : enchantez des items et débloquez le craft des bouteilles d'expérience</p>
<br>
<p>Les compétences sont équilibrées de façon à ce que vous atteignez les 50% un une heure dans les meilleures conditions.</p>

<br><hr><br>

<p><i class="fas fa-info-circle"></i><b> Les détails</b></p>
<br>
<p><b>-- Mineur --</b></p>
<p><u>Gagner des %</u> > Vous devez miner des minerais : charbon, redstone, lapis ou diamant. <border>0.02% / Minerais</border></p>
<p><u>- 50%</u> > Casse en 1×2 <border>0.01% / Minerais</border></p>
<p><u>- 75%</u> > Casse en 1×2×2 <border>0.005% / Minerais</border></p>
<p><u>- 100%</u> > Casse en 3×3
<br>
<p><b>-- Bucheron --</b></p>
<p><u>Gagner des %</u> > Vous devez casser des buches. <border>0.017% / Buche</border> Et <border>0.006 / Buche avec la Super Hache)</border></p>
<p><u>- 50%</u> > Casse 2 buches de hauteur</p>
<p><u>- 60%</u> > Casse 4 buches de hauteur</p>
<p><u>- 70%</u> > Casse 6 buches de hauteur</p>
<p><u>- 85%</u> > Casse 11 buches de hauteur</p>
<p><u>- 100%</u> > Casse toutes les buches</p>
<br>
<p><b>-- Fermier --</b></p>
<p><u>Gagner des %</u> > Vous devez casser des plantes : patates, carottes, blé, melons et pastèques. <border>0.02% / Plante</border></p>
<p><u>- 50%</u> > Houe en 3×3</p>
<p><u>- 75%</u> > Houe et place en 3×3 <border>0.01% / Plante</border></p>
<p><u>- 100%</u> > Houe, place et casse en 3×3</p>
<br>
<p><b>-- Chasseur --</b></p>
<p><u>Gagner des %</u> > Vous devez tuer des entités. <border>0.08% / Entitée</border> Puis, de 50 à 100% : <border>0.08 à 0.02% / Entitée</border></p>
<p><u>- 50%</u> > Spawners à zombie et cochon</p>
<p><u>- 60%</u> > + Spawners à araignée et vache</p>
<p><u>- 70%</u> > + Spawners à squelette et mouton</p>
<p><u>- 85%</u> > + Spawners à creeper et poule</p>
<p><u>- 100%</u> > + Spawners à enderman et chevaux</p>
<br>
<p><b>-- Enchanteur --</b></p>
<p><u>Gagner des %</u> > Vous devez enchanter des items. <border>XP Requis ÷ 10</border> Puis, de 50 à 100% : <border>XP Requis ÷ 10 à XP Requis ÷ 40</border></p>
<p><u>- [50% ; 100%]</u> >  Fiole d'expérience × 1 à 64</p>
<p><i>× 1 à 50%, × 64 à 100%, × 32 à 75% etc.</i></p>
